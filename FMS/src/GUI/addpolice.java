    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:
        try{
            
            int traffic_officer_id1 = Integer.parseInt(traffic_officer_id.getText());
            String q1 = "SELECT * FROM `police_officer` WHERE traffic_officer_id = '" +traffic_officer_id1+"'";
            PreparedStatement pst1 = myConn.prepareStatement(q1);
            ResultSet rs = pst1.executeQuery();
            String traffic_officer_email1 = ""; 
            String traffic_officer_full_name1 = "";
            String traffic_officer_password1 = "";
            String police_station1 = "";
            while(rs.next()) {
                 traffic_officer_email1= rs.getString("traffic_officer_email");
                 traffic_officer_full_name1=  rs.getString("traffic_officer_full_name");
                 traffic_officer_password1 = rs.getString("traffic_officer_password");
                 police_station1 = rs.getString("police_station");
                 
            }
            traffic_officer_email.setText(traffic_officer_email1);
            traffic_officer_full_name.setText(traffic_officer_full_name1);
            traffic_officer_password.setText(traffic_officer_password1);
            police_station.setText(police_station1);
            
        }
        catch(Exception e1){
            System.out.println("The error was" + e1);
        }
    }//GEN-LAST:event_SearchActionPerformed

