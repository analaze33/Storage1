    private void atpobuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atpobuttonActionPerformed
        // TODO add your handling code here:
        try{
            if(  traffic_officer_email.getText().equals("")
                 ||traffic_officer_full_name.getText().equals("")||traffic_officer_password.getText().equals("")
                 ||police_station.getText().equals("")){
                JOptionPane.showMessageDialog(null,"Please ensure to fill out all necessary informations!");
            }
            else{
                String save_police_officer_query = "INSERT INTO police_officer("+"`traffic_officer_email`,`traffic_officer_full_name`,`traffic_officer_password`,`police_station`)"
                     +"VALUES('"+traffic_officer_email.getText()+"','"+traffic_officer_full_name.getText()+"','"
                     +traffic_officer_password.getText()+"','"
                     +police_station.getText()+"')";
                PreparedStatement pst = myConn.prepareStatement(save_police_officer_query); 
                pst.execute();
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "The police officer was not saved!");
            System.out.println("The error was" + e);
        }
        
    }//GEN-LAST:event_atpobuttonActionPerformed

