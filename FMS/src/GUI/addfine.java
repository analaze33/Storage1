    private void ifbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ifbuttonActionPerformed
        // TODO add your handling code here:
                try{
            if(  fine_issue_date.getDate().equals("")
                 ||fine_expire_date.getDate().equals("")
                 ||provision.getText().equals("")||fine_amount.getText().equals("")){
                JOptionPane.showMessageDialog(null,"Please ensure to fill out all necessary informations!");
            }
            else{
                String save_driver_query = "INSERT INTO fine("
                     +"`driver_full_name`,`driver_email`,`class_of_vehicle`,`vehicle_no`,`traffic_officer_id`,`police_station`,`officer_name`,`fine_issue_date`"
                        + ",`fine_expire_date`,`provision`,`fine_amount`)"
                     +"VALUES('"+driver_full_name.getText()+"','"+driver_email.getText()+"','"+class_vehicle.getText()+"','"
                     +vehicle_no.getText()+"','"+traffic_officer_id.getText()+"','"+police_station.getText()+"','"+officer_name.getText()+"','"
                     +fine_issue_date.getDate()+"','"+fine_expire_date.getDate()+"','"
                     +provision.getText()+"','"+fine_amount.getText()+"')";
                PreparedStatement pst = myConn.prepareStatement(save_driver_query); 
                pst.execute();
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "The fine was not saved!");
            System.out.println("The error was" + e);
        }
    }//GEN-LAST:event_ifbuttonActionPerformed

