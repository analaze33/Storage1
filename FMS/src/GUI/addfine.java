    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        try  {
            traffic_officer_id.setText(POlogin.poff_id.getText());
            int driving_license_id1 = Integer.parseInt(driving_license_id.getText());
            int police_id1 = Integer.parseInt(traffic_officer_id.getText());
            String q1 = "SELECT * FROM driver WHERE license_id = '" + driving_license_id1 +"'";
            
            PreparedStatement pst1 = myConn.prepareStatement(q1);
            
            ResultSet rs1 = pst1.executeQuery();
            
            String driver_full_name1 = "";
            String driver_email1 = "";
            String class_of_vehicle1 = ""; 
            int vehicle_no1 = 0;
            while(rs1.next()) {
                driver_full_name1 = rs1.getString("driver_full_name");
                driver_email1 = rs1.getString("driver_email");
                class_of_vehicle1 = rs1.getString("class_of_vehicle");
                vehicle_no1 = rs1.getInt("vehicle_no");
            }
            driver_full_name.setText(driver_full_name1);
            driver_email.setText(driver_email1);
            class_vehicle.setText(class_of_vehicle1);
            String vehicle_no2 = Integer.toString(vehicle_no1);
            vehicle_no.setText(vehicle_no2);
            
            String q2 = "SELECT * FROM police_officer WHERE traffic_officer_id = '" + police_id1+"'";
            PreparedStatement pst2 = myConn.prepareStatement(q2);
            ResultSet rs2 = pst2.executeQuery();
            String police_station1 = "";
            String officer_name1 = "";
            while(rs2.next()) {
                police_station1 = rs2.getString("police_station"); 
                officer_name1 = rs2.getString("traffic_officer_full_name");
                
            }
            police_station.setText(police_station1);
            officer_name.setText(officer_name1); 
            
            
        }
         
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "The police officer was not loged in!");
            System.out.println("The error was" + e);
        }
    }//GEN-LAST:event_searchActionPerformed

